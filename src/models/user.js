const mongoose = require("mongoose");

//Creating USer Schema
const userSchema = new mongoose.Schema({
  firstName: {
    type: String,
  },
  lastName: {
    type: String,
  },
  email: {
    type: String,
  },
  password: {
    type: String,
  },
  age: {
    type: Number,
  },
  gender: {
    type: String,
  },
});

//Creating User Model
const User = mongoose.model("User", userSchema);

module.exports = User;
